function add(a,b){
    let result =  a + b;
    return result
}
function subtract(a,b){
    let result = a - b;
    return result
}
function multiply(a,b){
    let result = a * b
    return result
}
function divide(a,b){
    let result = a / b
    return result
}
function increment(n){
    return n + 1;
}
function decrement(n){
    return n -1;
}
function makeInt(n){
   return  parseInt(n, 10);
}
function preserveDecimal(n){
    return parseFloat(n)
}